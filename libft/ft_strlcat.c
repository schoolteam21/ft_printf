#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	dsize;
	size_t	ssize;
	size_t	i;

	dsize = ft_strlen(dst);
	ssize = ft_strlen(src);
	if (dsize > size)
		return (ssize + size);
	i = 0;
	while (i < dsize)
		i++;
	while (*src && i < size - 1)
	{
		dst[i] = *src;
		i++;
		src++;
	}
	dst[i] = '\0';
	return (ssize + dsize);
}
